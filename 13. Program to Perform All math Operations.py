import math

num = 16
x, y = 5, 3

# Square root and power
sqrt_val = math.sqrt(num)
power_val = math.pow(x, y)

# Trigonometric functions
sin_val = math.sin(math.radians(30))
cos_val = math.cos(math.radians(60))

# Logarithmic functions
log_val = math.log(10)
log10_val = math.log10(100)

# Constants
pi_val = math.pi
e_val = math.e

print("Square Root:", sqrt_val)
print("Power:", power_val)
print("Sine of 30:", sin_val)
print("Cosine of 60:", cos_val)
print("Log of 10:", log_val)
print("Log10 of 100:", log10_val)
print("Pi:", pi_val)
print("Euler's Number:", e_val)
